<?php

declare(strict_types=1);

/**
 * docs-sync — generate the drift-prone documentation fragments from source code.
 *
 * Produces three Markdown partials under website/src/content/docs/_generated/
 * (imported by the Starlight reference pages) and refreshes the compatibility
 * block in README.md between <!-- BEGIN:compatibility --> / <!-- END:compatibility -->.
 *
 * Sources of truth (never hand-edit the generated output):
 *   - Compatibility : composer.json + .github/workflows/test.yml matrix (honouring include/exclude)
 *   - Options       : the command $signature, parsed via Illuminate\Console\Parser
 *   - Unsupported   : BlueprintUnsupportedInterface constants
 *
 * Usage:
 *   php bin/docs-sync.php           # write the fragments and README block
 *   php bin/docs-sync.php --check   # exit non-zero if anything is out of date (CI gate)
 */

use Albertoarena\LaravelEventSourcingGenerator\Console\Commands\MakeEventSourcingDomainCommand;
use Albertoarena\LaravelEventSourcingGenerator\Domain\Blueprint\Contracts\BlueprintUnsupportedInterface;
use Illuminate\Console\Parser;
use Symfony\Component\Yaml\Yaml;

require __DIR__.'/../vendor/autoload.php';

const ROOT = __DIR__.'/..';
const GENERATED_DIR = ROOT.'/website/src/content/docs/_generated';
const BANNER = "<!-- AUTO-GENERATED by `composer docs:sync` — do not edit by hand -->\n\n";

/**
 * The one editorial input that is not encoded in code: which Laravel majors are
 * deprecated. Keep this in sync with the README wording. Everything else is derived.
 */
const DEPRECATED_LARAVEL = ['11'];

$check = in_array('--check', $argv, true);

$targets = [
    GENERATED_DIR.'/compatibility.md' => BANNER.buildCompatibilityTable(),
    GENERATED_DIR.'/options.md' => BANNER.buildOptionsTable(),
    GENERATED_DIR.'/unsupported-columns.md' => BANNER.buildUnsupportedList(),
];

$readmePath = ROOT.'/README.md';
$readmeNew = injectReadmeCompatibility($readmePath, buildCompatibilityTable());

$drift = [];

foreach ($targets as $path => $content) {
    if (! isCurrent($path, $content)) {
        $drift[] = relative($path);
        if (! $check) {
            @mkdir(dirname($path), 0o755, true);
            file_put_contents($path, $content);
        }
    }
}

if ($readmeNew !== null && ! isCurrentString($readmePath, $readmeNew)) {
    $drift[] = relative($readmePath).' (compatibility block)';
    if (! $check) {
        file_put_contents($readmePath, $readmeNew);
    }
}

if ($check) {
    if ($drift) {
        fwrite(STDERR, "Documentation is out of date. Run `composer docs:sync` and commit:\n");
        foreach ($drift as $d) {
            fwrite(STDERR, "  - $d\n");
        }
        exit(1);
    }
    echo "Documentation is up to date.\n";
    exit(0);
}

if ($drift) {
    echo "Updated:\n";
    foreach ($drift as $d) {
        echo "  - $d\n";
    }
} else {
    echo "Nothing to update — documentation already current.\n";
}
exit(0);

// ---------------------------------------------------------------------------

function buildCompatibilityTable(): string
{
    $composer = json_decode((string) file_get_contents(ROOT.'/composer.json'), true, 512, JSON_THROW_ON_ERROR);
    $phpConstraint = $composer['require']['php'] ?? '';
    $spatie = $composer['require']['spatie/laravel-event-sourcing'] ?? '';

    $matrix = Yaml::parseFile(ROOT.'/.github/workflows/test.yml')['jobs']['test']['strategy']['matrix'] ?? [];

    $phpAxis = array_map(fn ($v) => normalizeVersion((string) $v), $matrix['php'] ?? []);
    $laravelAxis = array_map(fn ($v) => majorOf((string) $v), $matrix['laravel'] ?? []);

    // laravel major => testbench major, from matrix.include
    $testbench = [];
    foreach ($matrix['include'] ?? [] as $inc) {
        if (isset($inc['laravel'], $inc['testbench'])) {
            $testbench[majorOf((string) $inc['laravel'])] = majorOf((string) $inc['testbench']);
        }
    }

    // excluded (laravel major, php) pairs, from matrix.exclude
    $excluded = [];
    foreach ($matrix['exclude'] ?? [] as $ex) {
        if (isset($ex['laravel'], $ex['php'])) {
            $excluded[majorOf((string) $ex['laravel']).'@'.normalizeVersion((string) $ex['php'])] = true;
        }
    }

    sort($laravelAxis, SORT_NATURAL);
    sort($phpAxis, SORT_NATURAL);

    $rows = [];
    foreach ($laravelAxis as $lv) {
        $php = array_values(array_filter($phpAxis, fn ($p) => ! isset($excluded["$lv@$p"])));
        $label = in_array($lv, DEPRECATED_LARAVEL, true) ? "$lv.x (deprecated)" : "$lv.x";
        $tb = isset($testbench[$lv]) ? $testbench[$lv].'.x' : '—';
        $rows[] = sprintf('| %s | %s | %s |', $label, implode(', ', $php), $tb);
    }

    $table = "| Laravel | PHP | Testbench |\n| --- | --- | --- |\n".implode("\n", $rows)."\n";

    $phpRange = phpRange($phpConstraint ?: implode('|', array_map(fn ($p) => "^$p", $phpAxis)));
    $footer = sprintf(
        "\n**PHP:** %s · **Spatie Laravel Event Sourcing:** %s\n",
        $phpRange,
        constraintToXRange($spatie)
    );

    return $table.$footer;
}

function buildOptionsTable(): string
{
    $ref = new ReflectionClass(MakeEventSourcingDomainCommand::class);
    $signature = (string) $ref->getDefaultProperties()['signature'];

    [, $arguments, $options] = Parser::parse($signature);

    $argLines = [];
    foreach ($arguments as $arg) {
        $required = $arg->isRequired() ? 'required' : 'optional';
        $argLines[] = sprintf('- `%s` — %s (%s)', $arg->getName(), cell($arg->getDescription() ?: 'The model name'), $required);
    }

    $rows = [];
    foreach ($options as $opt) {
        $name = '`--'.$opt->getName().'`';
        $short = $opt->getShortcut() ? '`-'.$opt->getShortcut().'`' : '—';
        if (! $opt->acceptValue()) {
            $default = 'flag';
        } else {
            $d = $opt->getDefault();
            $default = ($d === null || $d === '') ? '—' : '`'.$d.'`';
        }
        $rows[] = sprintf('| %s | %s | %s | %s |', $name, $short, $default, cell($opt->getDescription()));
    }

    $out = '';
    if ($argLines) {
        $out .= "**Arguments**\n\n".implode("\n", $argLines)."\n\n";
    }
    $out .= "**Options**\n\n";
    $out .= "| Option | Shortcut | Default | Description |\n| --- | --- | --- | --- |\n".implode("\n", $rows)."\n";

    return $out;
}

function buildUnsupportedList(): string
{
    $types = BlueprintUnsupportedInterface::UNSUPPORTED_COLUMN_TYPES;
    $skipped = BlueprintUnsupportedInterface::SKIPPED_METHODS;
    sort($types, SORT_NATURAL);
    sort($skipped, SORT_NATURAL);

    $out = "The following Blueprint column types are not yet supported:\n\n";
    $out .= implode("\n", array_map(fn ($t) => "- `$t`", $types))."\n\n";
    $out .= "The following Blueprint methods are skipped (indexes, foreign keys and soft deletes are not relevant to the projection):\n\n";
    $out .= implode("\n", array_map(fn ($t) => "- `$t`", $skipped))."\n";

    return $out;
}

// --- helpers ---------------------------------------------------------------

function injectReadmeCompatibility(string $path, string $block): ?string
{
    if (! is_file($path)) {
        return null;
    }
    $readme = (string) file_get_contents($path);
    $begin = '<!-- BEGIN:compatibility -->';
    $end = '<!-- END:compatibility -->';
    if (! str_contains($readme, $begin) || ! str_contains($readme, $end)) {
        return null; // markers not present yet; README slim-down step adds them
    }
    $pattern = '/'.preg_quote($begin, '/').'.*?'.preg_quote($end, '/').'/s';
    $replacement = $begin."\n\n".rtrim($block)."\n\n".$end;

    return preg_replace($pattern, addcslashes($replacement, '\\$'), $readme);
}

function isCurrent(string $path, string $content): bool
{
    return is_file($path) && file_get_contents($path) === $content;
}

function isCurrentString(string $path, string $content): bool
{
    return is_file($path) && file_get_contents($path) === $content;
}

/** Escape pipes and collapse whitespace so text is safe inside a Markdown table cell. */
function cell(string $text): string
{
    return trim(str_replace('|', '\\|', preg_replace('/\s+/', ' ', $text) ?? $text));
}

function relative(string $path): string
{
    return ltrim(str_replace(realpath(ROOT) ?: ROOT, '', realpath($path) ?: $path), '/');
}

function normalizeVersion(string $v): string
{
    return trim(str_replace(['*', 'x'], '', rtrim($v, '.* ')), '.');
}

function majorOf(string $v): string
{
    return (string) (int) ltrim($v, '^~');
}

function constraintToXRange(string $constraint): string
{
    $major = majorOf($constraint);

    return $major !== '0' ? "$major.x" : trim($constraint);
}

function phpRange(string $constraint): string
{
    preg_match_all('/\d+\.\d+/', $constraint, $m);
    $versions = $m[0] ?? [];
    if (! $versions) {
        return trim($constraint);
    }
    sort($versions, SORT_NATURAL);

    return count($versions) === 1 ? $versions[0] : $versions[0].' – '.end($versions);
}
